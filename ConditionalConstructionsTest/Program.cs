            Console.WriteLine("Введите значение для второй переменной");
            // Считываем ответ и записываем его в переменную number2 
            int number2 = Convert.ToInt32(Console.ReadLine());
            // Выводим на экран информацию о введеных числах
            Console.WriteLine($"Первое число {number1} второе {number2}");
