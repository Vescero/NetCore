            switch (selection)
            {
                // Если ввели 1
                case 1:
                    // Считаем пример с сложением
                    number1 += number2;
                    // Вывод на экран результат операции
                    Console.WriteLine($"Вы выбрали сложение, результат: {number1}");
                    // Выход из условия
                    break;
                // Если ввели 2
                case 2:
                    // Считаем пример с вычитанием
                    number1 -= number2;
                    // Вывод на экран результат операции
                    Console.WriteLine($"Вы выбрали вычитание, результат: {number1}");
                    // Выход из условия
                    break;
                // Если ввели 3
                case 3:
                    // Считаем пример с умножением
                    number1 *= number2;
                    // Вывод на экран результат операции
                    Console.WriteLine($"Вы выбрали умножение, результат: {number1}");
                    // Выход из условия
                    break;
                // Если ввели любое другое значение
                default:
                    // Вывод на сообщения об ошибке
                    Console.WriteLine("Вы ввели недопустимое значение");
                    // Выход из условия
                    break;
            }

        }                                   // Конец объявления метода CalcWithInput
    }                                       // Конец объявления класса Program
}                                           // Конец объявления пространства имён
